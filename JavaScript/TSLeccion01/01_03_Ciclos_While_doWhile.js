let contador = 0
while(contador >3){
    console.log(contador);
    contador++;
}
console.log("Fin de ciclo");

//do while

let conteo = 0
do {
    console.log(conteo)
    conteo++;
}while(conteo >3)
console.log("Fin de ciclo do while");

///for
for(let contador = 0; contador <3 ; contador++){
    console.log(contador)
}
console.log("Fin de For");